async function populateBrands(industryName: string, brandsData: BrandData[]) {
  console.log(`\n${'='.repeat(70)}`);
  console.log(`🚀 Populating: ${industryName}`);
  console.log(`${'='.repeat(70)}\n`);

  let successCount = 0;
  let skipCount = 0;
  let errorCount = 0;

  for (const brandData of brandsData) {
    try {
      console.log(`Processing: ${brandData.name}...`);

      const existing = await db.query.brands.findFirst({
        where: eq(brands.domain, brandData.domain),
      });

      if (existing) {
        console.log(`  ⏭️  Already exists, skipping\n`);
        skipCount++;
        continue;
      }

      await db.insert(brands).values({
        organizationId: BENCHMARK_ORG_ID,
        ...brandData,
        isBenchmark: true,
        lastEnrichedAt: new Date(),
        monitoringEnabled: false,
        isActive: true,
      });

      console.log(`  ✅ Inserted successfully\n`);
      successCount++;

      await new Promise(resolve => setTimeout(resolve, 300));

    } catch (error) {
      console.error(`  ❌ Error inserting ${brandData.name}:`, error);
      errorCount++;
    }
  }

  console.log(`\n📊 ${industryName} Summary:`);
  console.log(`✅ Success: ${successCount} | ⏭️  Skipped: ${skipCount} | ❌ Errors: ${errorCount}\n`);

  return { successCount, skipCount, errorCount };
}

async function main() {
  console.log('\n🌟 POPULATING ALL BENCHMARK BRANDS\n');
  console.log(`Industries: ${Object.keys(ALL_BRANDS).length}`);
  console.log(`Total Brands: ${Object.values(ALL_BRANDS).flat().length}\n`);

  const allResults = {
    totalSuccess: 0,
    totalSkipped: 0,
    totalErrors: 0,
  };

  for (const [industryName, brandsData] of Object.entries(ALL_BRANDS)) {
    const results = await populateBrands(industryName, brandsData);
    allResults.totalSuccess += results.successCount;
    allResults.totalSkipped += results.skipCount;
    allResults.totalErrors += results.errorCount;
  }

  console.log(`\n${'='.repeat(70)}`);
  console.log(`🎉 GRAND TOTAL`);
  console.log(`${'='.repeat(70)}`);
  console.log(`✅ Successfully inserted: ${allResults.totalSuccess}`);
  console.log(`⏭️  Skipped (already exist): ${allResults.totalSkipped}`);
  console.log(`❌ Errors: ${allResults.totalErrors}`);
  console.log(`Total: ${allResults.totalSuccess + allResults.totalSkipped + allResults.totalErrors}`);
  console.log(`${'='.repeat(70)}\n`);
}

